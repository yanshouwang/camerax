final class PersonName {
  final String? formattedName;
  final String? pronunciation;
  final String? prefix;
  final String? first;
  final String? middle;
  final String? last;
  final String? suffix;

  PersonName({
    required this.formattedName,
    required this.pronunciation,
    required this.prefix,
    required this.first,
    required this.middle,
    required this.last,
    required this.suffix,
  });
}
